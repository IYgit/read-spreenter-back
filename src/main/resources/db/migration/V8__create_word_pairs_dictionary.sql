-- Pairs where the two words are DIFFERENT (player must click these)
CREATE TABLE wp_diff_pairs (
    id    BIGSERIAL PRIMARY KEY,
    word1 VARCHAR(50) NOT NULL,
    word2 VARCHAR(50) NOT NULL
);

-- Single words used to make IDENTICAL pairs (word shown twice — player must NOT click)
CREATE TABLE wp_same_words (
    id   BIGSERIAL PRIMARY KEY,
    word VARCHAR(50) NOT NULL UNIQUE
);

